export class vec3 {
    constructor(X, Y, Z) {
        this.X = X;
        this.Y = Y;
        this.Z = Z;
    }
}

export class vec4 {
    constructor(X, Y, Z, W) {
        this.X = X;
        this.Y = Y;
        this.Z = Z;
        this.W = W;
    }
}

export class vec2 {
    constructor(X, Y) {
        this.X = X;
        this.Y = Y;
    }
}